public class HangmanPrinter {

    private String[] printStages = {
            """
            --------|--------
            |       |
            |       |
            |       
            |
            |
            |
            |
            | 
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○    
            |
            |
            |
            |
            | 
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○
            |       ◊
            |
            |
            |
            |
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○
            |     /-◊
            |
            |
            |
            |
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○
            |     /-◊-\\
            |
            |
            |
            |
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○
            |     /-◊-\\
            |      /
            |
            |
            |
            """,
            """
            --------|--------
            |       |
            |       |
            |       ○
            |     /-◊-\\
            |      /\\
            |
            |
            |
            """
    };

    public void printConstruct(int n){
        System.out.println(printStages[n-1] + "\n" +
                "Осталось " + (7-n) + " попыток\n");
    }
}
